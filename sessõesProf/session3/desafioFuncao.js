// Entrada: um numero inteiro
// Saída: o quadrado do numero inteiro recebido

function devolverQuadrado(numero) {

    return numero * numero;
}

// Entrada: um numero inteiro
// Saida: esse número - 5
function subtrairCinco(numero) {

    return numero - 5;
}


console.log(devolverQuadrado(5));
const quadrado = devolverQuadrado(5);
console.log(subtrairCinco(quadrado));